
grade = int(input("Enter your grade: "))
# Determine the letter grade
if grade >= 90:
    print("The letter grade is A.")
elif grade >= 80:
    print("The letter grade is B.")
elif grade >= 70:
    print("The letter grade is C.")
elif grade >= 60:
    print("The letter grade is D.")
elif grade < 60:
    print("The letter grade is F.")
else:
    print("Invalid grade.")




